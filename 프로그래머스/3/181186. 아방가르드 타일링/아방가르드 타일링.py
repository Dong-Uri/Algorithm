from pprint import pprint

def solution(n):
    answer = 0
    case = ['000', '012', '210', '102', '201', '120', '021']
    dp = [dict.fromkeys(case, 0) for _ in range(4)]
    dp[0]['000']= 1
    l = 0
    while l < n:
        dp[0]['000'] %= 1000000007
        dp[0]['012'] %= 1000000007
        dp[0]['210'] %= 1000000007
        dp[0]['102'] %= 1000000007
        dp[0]['201'] %= 1000000007
        dp[0]['120'] %= 1000000007
        dp[0]['021'] %= 1000000007
        # 000
        dp[1]['000'] += dp[0]['000']
        dp[1]['102'] += dp[0]['000']
        dp[1]['201'] += dp[0]['000']
        dp[1]['012'] += dp[0]['000']
        dp[1]['210'] += dp[0]['000']
        dp[2]['000'] += dp[0]['000']
        dp[2]['000'] += dp[0]['000']
        dp[3]['000'] += dp[0]['000']
        # 012
        dp[1]['201'] += dp[0]['012']
        dp[2]['000'] += dp[0]['012']
        # 210
        dp[1]['102'] += dp[0]['210']
        dp[2]['000'] += dp[0]['210']
        # 102
        dp[1]['021'] += dp[0]['102']
        dp[2]['000'] += dp[0]['102']
        # 201
        dp[1]['120'] += dp[0]['201']
        dp[2]['000'] += dp[0]['201']
        # 120
        dp[1]['012'] += dp[0]['120']
        # 021
        dp[1]['210'] += dp[0]['021']
        dp = dp[1:] + [dict.fromkeys(case, 0)]
        l += 1
    return dp[0]['000'] % 1000000007